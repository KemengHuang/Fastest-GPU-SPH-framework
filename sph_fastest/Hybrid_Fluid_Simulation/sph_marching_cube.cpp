inline float3 my_Normalize(float3 v)
{
    float n = v.x*v.x + v.y*v.y + v.z*v.z;
    float3 v_n = make_float3(0.0f, 0.0f, 0.0f);
    if (n != 0.0) {
        n = sqrt(n);
        v_n.x = v.x / n; v_n.y = v.y / n; v_n.z = v.z / n;
    }
    return v_n;
}

