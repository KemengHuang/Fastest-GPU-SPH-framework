inline void set_cellvertex(cellvertex* v, float* density, float stride, int width, int height, int xn, int yn, int zn)
{
    int wh = width*height;
    int base = xn + yn*width + zn*width*height;
    v->n.x = -(density[base + 1] - density[base - 1]);
    v->n.y = -(density[base + width] - density[base - width]);
    v->n.z = -(density[base + wh] - density[base - wh]);

    v->val = density[base];
    v->p.x = xn*stride;
    v->p.y = yn*stride;
    v->p.z = zn*stride;
}

inline void set_vertex(float3* p, float3* n, cellvertex* v0, cellvertex* v1, float isolevel)
{
    float mu;
    if (std::abs(isolevel - v0->val) < EPSILON)
    {
        *n = v0->n;
        *p = v0->p;
        return;
    }

    if (std::abs(isolevel - v1->val) < EPSILON)
    {
        *n = v1->n;
        *p = v1->p;
        return;
    }

    if (std::abs(v0->val - v1->val) < EPSILON)
    {
        *n = v0->n;
        *p = v0->p;
        return;
    }

    mu = (isolevel - v0->val) / (v1->val - v0->val);

    p->x = v0->p.x + mu * (v1->p.x - v0->p.x);
    p->y = v0->p.y + mu * (v1->p.y - v0->p.y);
    p->z = v0->p.z + mu * (v1->p.z - v0->p.z);

    n->x = v0->n.x + mu * (v1->n.x - v0->n.x);
    n->y = v0->n.y + mu * (v1->n.y - v0->n.y);
    n->z = v0->n.z + mu * (v1->n.z - v0->n.z);
}

