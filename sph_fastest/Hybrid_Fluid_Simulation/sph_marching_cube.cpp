bool generateMesh(float3 *pos, unsigned int nump, sph::SystemParameter *sys_para, unsigned int loop_times)
{
    long x, y, z;
    int mc_range = 2;

    float fmin_x, fmax_x, fmin_y, fmax_y, fmin_z, fmax_z;
    long min_x, max_x, min_y, max_y, min_z, max_z;

    float iso_radius = 0.04f;
    float stride = iso_radius / 4;
    float stride2 = stride*stride;
    float inv_stride = 1.0f / stride;

    fmin_x = fmin_y = fmin_z = std::numeric_limits<float>::max();
    fmax_x = fmax_y = fmax_z = std::numeric_limits<float>::max() * -1.0f;

    for (size_t i = 0; i < nump; ++i)
    {
        // skip non-liquid particles
        // if (xxxxx) continue;

        if (fmin_x > pos[i].x) fmin_x = pos[i].x;
        if (fmax_x < pos[i].x) fmax_x = pos[i].x;
        if (fmin_y > pos[i].y) fmin_y = pos[i].y;
        if (fmax_y < pos[i].y) fmax_y = pos[i].y;
        if (fmin_z > pos[i].z) fmin_z = pos[i].z;
        if (fmax_z < pos[i].z) fmax_z = pos[i].z;
    }

    corner = make_float3(fmin_x - mc_range*stride, fmin_y - mc_range*stride, fmin_z - mc_range*stride);
    min_x = 0;
    min_y = 0;
    min_z = 0;
    inv_stride = 1.0f / stride;
    max_x = (long)((fmax_x - fmin_x)*inv_stride);
    max_y = (long)((fmax_y - fmin_y)*inv_stride);
    max_z = (long)((fmax_z - fmin_z)*inv_stride);

    double cell_size = stride;
    mc_range = 4;
    mc_stride = stride;
    width = max_x + mc_range * 2 + 6;
    height = max_y + mc_range * 2 + 6;
    depth = max_z + mc_range * 2 + 6;

    if (nullptr != density) return false;
    unsigned int capacity = width*height*depth;
    density = new float[capacity];
    std::memset(density, 0, capacity * sizeof(float));

    for (size_t i = 0; i < nump; ++i)
    {
        // skip non-liquid particles
        // if (xxxxx) continue;

        float3 piv = pos[i] - corner;
        piv = piv * float(inv_stride);

        for (z = -mc_range; z <= mc_range; z++)
        {
            for (y = -mc_range; y <= mc_range; y++)
            {
                for (x = -mc_range; x <= mc_range; x++)
                {
                    long vindex;
                    double h2_r2, dx, dy, dz;
                    dx = ((long)piv.x)*stride + corner.x - pos[i].x + stride*x;
                    dy = ((long)piv.y)*stride + corner.y - pos[i].y + stride*y;
                    dz = ((long)piv.z)*stride + corner.z - pos[i].z + stride*z;

                    h2_r2 = iso_radius*iso_radius - (dx*dx + dy*dy + dz*dz);
                    if (h2_r2 > 0)
                    {
                        vindex = (int)(piv.x + x - min_x + 2) + ((int)(piv.y + y - min_y + 2))*width + ((int)(piv.z + z - min_z + 2))*width*height;
                        if (vindex<0 || vindex>width*height*depth) continue;

                        float kernelValue = sys_para->poly6_value * pow(h2_r2, 3);
                        density[vindex] += kernelValue * sys_para->mass;
                    }
                }
            }
        }
    }

    return true;
}

